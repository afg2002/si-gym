    protected void datatable(){
        Object[] Baris = {"id_pemilihan","id_instruktur","nama_instruktur","id_member","nama_member","bidang"};
        tabmode  = new DefaultTableModel(null, Baris);
        tabPemilihanIns.setModel(tabmode);
        String sql = "SELECT pemilihan.id_pemilihan,pemilihan.id_instruktur,instruktur.nama_instruktur,pemilihan."
                + "id_member,member.nama_member,pemilihan.bidang FROM pemilihan INNER JOIN instruktur"
                + " ON pemilihan.id_instruktur = instruktur.id_instruktur INNER JOIN member ON pemilihan.id_member = "
                + "member.id_member";
        try{
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while(res.next()){
                String a = res.getString("id_pemilihan");
                String b= res.getString("id_instruktur");
                String d= res.getString("nama_instruktur");
                String e = res.getString("id_member");
                String f = res.getString("nama_member");
                String g = res.getString("bidang");
                
                String[] data = {a,b,d,e,f,g};
                tabmode.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(pemilihanIns.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
