    protected void datatable(){
        Object[] Baris = {"id_presensi","id_member","nama_member","jenis_kelamin","hari_masuk","jam_masuk","tgl_masuk"};
        tabmode  = new DefaultTableModel(null, Baris);
        tabInstruktur.setModel(tabmode);
        String sql = "SELECT absensi.id_presensi,absensi.id_member,member."
                + "nama_member,member.jenis_kelamin,absensi.hari_masuk,absensi.jam_masuk,absensi.tgl_masuk "
                + "FROM absensi INNER JOIN member ON absensi.id_member = member.id_member;";
        try{
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while(res.next()){
                String a = res.getString("id_presensi");
                String b= res.getString("id_member");
                String d= res.getString("nama_member");
                String e = res.getString("jenis_kelamin");
                String f = res.getString("hari_masuk");
                String g = res.getString("jam_masuk");
                String h = res.getString("tgl_masuk");
                
                String[] data = {a,b,d,e,f,g,h};
                tabmode.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(absensi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
