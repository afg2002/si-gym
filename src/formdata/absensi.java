            String sql = "SELECT absensi.id_presensi,absensi.id_member,member.nama_member,"
                    + "member.jenis_kelamin,absensi.hari_masuk,absensi."
                    + "jam_masuk,absensi.tgl_masuk "
                    + "FROM absensi INNER JOIN member ON absensi.id_member = member.id_member;";
