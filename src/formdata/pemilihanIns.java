    private void bCariPemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCariPemActionPerformed
            String id_pem = tIDPemilihan.getText();
            String sql = "SELECT pemilihan.id_instruktur,instruktur.nama_instruktur,pemilihan.id_member,member.nama_member,pemilihan.bidang "
                    + "FROM pemilihan INNER JOIN instruktur ON pemilihan.id_instruktur = instruktur.id_instruktur"
                    + " INNER JOIN member ON pemilihan.id_member = member.id_member WHERE pemilihan.id_pemilihan = '" + id_pem + "'" ;
            try{
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            if(res.next()){
                String a = res.getString("id_instruktur");
                String b = res.getString("nama_instruktur");
                String c = res.getString("id_member");
                String d = res.getString("nama_member");
                String e = res.getString("bidang");
                
                tIdInstruktur.setText(a);
                tNama.setText(b);
                tIDMember.setText(c);
                tNama1.setText(d);
                tBidang.setSelectedItem(e);
            }else{
                reset();
            }
            } catch (SQLException ex) {
                Logger.getLogger(member.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_bCariPemActionPerformed

