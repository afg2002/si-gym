    private void bCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCariActionPerformed
            String kode_alat = tIDInstruktur.getText();
            String sql = "SELECT * FROM instruktur WHERE instruktur = " + kode_alat;
            try{
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            if(res.next()){
                String a = res.getString("nama_instruktur");
                String b = res.getString("no_telp");
                String c = res.getString("jenis_kelamin");
                String d = res.getString("bidang");
                String e = res.getString("email");
                java.sql.Date f = res.getDate("tgl_beli");
                tNamaInstruktur.setText(a);
                tNoTelp.setText(b);
                tJK.setSelectedItem(c);
                tBidang.setSelectedItem(d);
                tEmail.setText(e);
                tTanggalJoin.setDate(f);
            }else{
                reset();
            }
            } catch (SQLException ex) {
                Logger.getLogger(member.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_bCariActionPerformed

