    protected void datatable(){
        Object[] Baris = {"id_instruktur","nama_instruktur","no_telp","jenis_kelamin","bidang","email","tanggal_join"};
        tabmode  = new DefaultTableModel(null, Baris);
        tabInstruktur.setModel(tabmode);
        String sql = "select * from instruktur";
        try{
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while(res.next()){
                String a = res.getString("id_instruktur");
                String b= res.getString("nama_instruktur");
                String d= res.getString("no_telp");
                String e = res.getString("jenis_kelamin");
                String f = res.getString("bidang");
                String g = res.getString("email");
                String h = res.getString("tanggal_join");
                
                String[] data = {a,b,d,e,f,g,h};
                tabmode.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(instruktur.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
