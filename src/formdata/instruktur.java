        String bidang = tBidang.getSelectedItem().toString();
        String email = tEmail.getText();
        java.sql.Date tgl_join = new java.sql.Date(tTanggalJoin.getDate().getTime());
        Object[] values = {id_Instruktur,nama_instruktur,no_telp,jenis_kelamin,bidang,email,tgl_join};
        
        try {
            db.insertData(conn, table, columns, values);
            datatable();
            reset();
        } catch (SQLException ex) {
            Logger.getLogger(instruktur.class.getName()).log(Level.SEVERE, null, ex);
        }
